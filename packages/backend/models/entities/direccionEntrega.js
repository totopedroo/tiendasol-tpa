export class DireccionEntrega {
  constructor(
    calle,
    altura,
    piso,
    departamento,
    codigoPostal,
    ciudad,
    provincia,
    pais,
    latitud,
    longitud,
  ) {
    this.calle = calle;
    this.altura = altura;
    this.piso = piso;
    this.departamento = departamento;
    this.codigoPostal = codigoPostal;
    this.ciudad = ciudad;
    this.provincia = provincia;
    this.pais = pais;
    this.latitud = latitud;
    this.longitud = longitud;
  }
}
